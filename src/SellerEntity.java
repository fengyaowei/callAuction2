public class SellerEntity {
    private Double ps;
    private int os;
    private boolean s;

    public Double getPs(){
        return this.ps;
    }
    public void setPs(Double ps){
        this.ps = ps;
    }

    public int getOs(){
        return this.os;
    }
    public void setOs(int os){
        this.os = os;
    }

    public boolean getS(){
        return this.s;
    }
    public void setS(Boolean s){
        this.s = s;
    }
}